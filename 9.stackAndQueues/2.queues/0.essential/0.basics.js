// First In First Out (FIFO)
// or
// Last In Last Out

/* 
    A queue is a linear data structure that follows a particular order
    in which the operations are performed. The order is First In First Out (FIFO) or
    Last In Last Out (LILO)
*/

/* 
    functions that queue can perfrom
        1. enqueue(element) ==> Responsible for adding the element inside the queue
        2. dequeue() ==> Responsible for taking out the element from the queue
        3. front()/ peek() ==> Gives the element at the front of our queue
        4. isEmpty() ==> if the queue is empty or not, returns boolean
        5. size() ==> howmany elements currently inside the queue
*/